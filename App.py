import streamlit as st

# تنظیمات اولیه صفحه
st.set_page_config(page_title="طراح آزمون هوش", layout="centered")

st.title("🎯 تولیدکننده آزمون هوش")

# ۱. جای سرچ برای مباحث
topics = st.text_input("مباحث آزمون را وارد کنید (مثلاً: هوش کلامی، ریاضی، تجسمی):")

# ۲. انتخاب درجه سختی
difficulty = st.radio(
    "درجه سختی را انتخاب کنید:",
    options=["انتخاب نشده", "آسان", "متوسط", "سخت"],
    index=0
)

# ۳. دکمه تولید آزمون
if st.button("تولید آزمون"):
    # بررسی خطا
    if difficulty == "انتخاب نشده":
        st.error("⚠️ لطفاً ابتدا یک درجه سختی را انتخاب کنید!")
    elif not topics:
        st.warning("لطفاً حداقل یک مبحث را وارد کنید.")
    else:
        st.success(f"در حال تولید آزمون در سطح {difficulty} برای مباحث: {topics}...")
        
        # اینجا تابعی فراخوانی می‌شود که به هوش مصنوعی دستور می‌دهد
        # سوالات را بر اساس منابع کمپلکس و مالتی پل طراحی کند.
        # ساختار خروجی: برای هر مبحث ۱۰ سوال
        
        generate_quiz(topics, difficulty)

def generate_quiz(topics, difficulty):
    # این تابع در نسخه نهایی با API هوش مصنوعی متصل می‌شود
    st.write(f"در حال تولید ۱۰ سوال برای هر بخش از مباحث '{topics}' با سطح علمی تیزهوشان...")
    # نمایش خروجی نمونه
    st.info("نمونه سوال ۱: [در اینجا سوالات تخصصی تیزهوشان نمایش داده خواهد شد]")
